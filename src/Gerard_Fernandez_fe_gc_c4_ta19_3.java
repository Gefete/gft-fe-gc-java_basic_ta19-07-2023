
public class Gerard_Fernandez_fe_gc_c4_ta19_3 {

	public static void main(String[] args) {
		int x=5;
		int y=2;
		double n=11.80;
		double m=4.50;
		
		//Mostrar todas las variables con su valor
		System.out.println("x = "+x);
		System.out.println("y = "+y);
		System.out.println("n = "+n);
		System.out.println("m = "+m);
		
		//operaciones entre variables
		System.out.println("X+Y = "+(x+y));
		System.out.println("X-Y = "+(x-y));
		System.out.println("X*Y = "+(x*y));
		System.out.println("X/Y = "+(x/y));
		System.out.println("X%Y = "+(x%y));
		System.out.println("N+M = "+(n+m));
		System.out.println("N-M = "+(n-m));
		System.out.println("N*M = "+(n*m));
		System.out.println("N/M = "+(n/m));
		System.out.println("N%M = "+(n%m));
		System.out.println("X+N = "+(x+n));
		System.out.println("Y/M = "+(y/m));
		System.out.println("Y%M = "+(y%m));
		System.out.println("Y%M = "+(y%m));
		
		//Dobles
		System.out.println("(El Doble) X*2 = "+(x*2));
		System.out.println("(El Doble) Y*2 = "+(y*2));
		System.out.println("(El Doble) N*2 = "+(n*2));
		System.out.println("(El Doble) M*2 = "+(m*2));
		
		//total de todas las variables
		System.out.println("(suma de todas la variable) X+Y+N+M = "+(x+y+n+m));
		
		//producto de todas las variables
		System.out.println("(producto de todas la variable) X*Y*N*M = "+(x*y*n*m));
	}

}
